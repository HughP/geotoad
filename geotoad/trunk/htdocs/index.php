		<div class="hacknewstitle">3.5.0 Final Released!</div>
		<div class="hacknewsdate">31jan2003 19:47</div>
Our first major release in 4 months is out! <a href="download.php">Download
it now!</a> Here are some of the new changes:

<ul>
<li>New Text-User Interface (TUI), which is the default instead of the
command-line interface.</li>
<li>Windows users no longer have to install Ruby. We now compile GeoToad as
an .exe just for them!</li>
<li>Greatly improved HTML, GPX, Text, and CSV output</li>
<li>Ability to do title keyword searches in addition to the body</li>
<li>Much faster downloading of geocache information.</li>
<li>Default waypoint size set to 16</li>
<li>Slowlink mode added for extra performance for dialup users</li>
<li>Duplicate waypoint id correction</li>
<li>Zipcode support for Canada and England</li>
<li>A boatload of bugfixes, reported by our fantastic userbase. Thanks guys!</li>
</ul>

I would like to personally thank Mike Capito and Scott Brynen who put a lot
of work into this release.  See the <a
href="http://toadstool.se/hacks/geotoad/files/ChangeLog.txt">ChangeLog</a>
for a  detailed list of changes.</p>
