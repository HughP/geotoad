 <div class="hacktext"> 
	<div class="hackpagetitle">Why GeoToad?</div>

		GeoToad is free software to help speed up the boring part of <a href="http://www.geocaching.com/">geocaching</a>: choosing the cache and collecting the data.
It is the perfect tool for geocacher's on the go! Ever feel:

		<ul>
		<li>Sick of cryptic waypoint names like &quot;GC5042&quot; on your GPS?
</li>
			<li>Wish you could store geocache details and hints on your cellphone, PDA, or iPod?</li>
			<li>Want to keep an up-to-date list of the hardest geocaches in your state on your GPS?</li>
			<li>Wish you could print out one long page with details on all the local caches you haven't found yet?</li>
		</li>
		</ul>


	<div class="hackpagetitle">Tell Me More!</div>
		<div class="offsetimage">
			<a href="screenshots.php" class="imagelink"><img src="screenshots/Sony-Ericsson-P800-small.jpg" width="163" height="250" border="2">
			<br />GeoToad Data on a Sony-Ericsson P800</a>
		</div>

		<p
>GeoToad lets you perform on-demand queries against the 
		<a href="http://www.geocaching.com/">Geocaching</a> website, and 
		export the matching geocaches to <a href="features.php">numerous devices</a> 
		and <a href="features.php">over 20 file formats</a>. You
tell it what you want to see, such as:</p>

		<div class="hackexample">Geocaches that I have not found within 15 miles of 27513 with travelbugs.</div>
	
		or if you are a little more demanding, something like:

		<div class="hackexample">Geocaches in Texas
with a difficulty of 3 or higher that have never been found, match the keywoards river,
or stream, but not any caches owned by Elvis.</div>

You can then take these query results with you on a nice long print-out,
import them into <a href="http://www.easygps.com/">EasyGPS</a>, transfer
them to your GPS unit, or even save the results onto your cellphone!

		<p>GeoToad supports Windows, 
		Mac OS X, Linux, and other UNIX flavors!</p>
</div>

